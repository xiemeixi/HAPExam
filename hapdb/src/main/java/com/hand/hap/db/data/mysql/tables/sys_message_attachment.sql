  COMMENT '创建日期',
  `LAST_UPDATED_BY`       BIGINT   DEFAULT -1
  COMMENT '最后被用户修改',
  `LAST_UPDATE_DATE`      DATETIME DEFAULT CURRENT_TIMESTAMP
  COMMENT '最后修改日期',
  `ATTRIBUTE1`            VARCHAR(255) COMMENT '属性1',
