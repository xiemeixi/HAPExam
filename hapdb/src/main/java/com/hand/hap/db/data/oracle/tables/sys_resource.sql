CREATE TABLE SYS_RESOURCE_B
(
  RESOURCE_ID           NUMBER                  NOT NULL,
  URL                   VARCHAR2(400)           NOT NULL,
  TYPE                  VARCHAR2(15),
  NAME                  VARCHAR2(40)            NOT NULL,
  DESCRIPTION           VARCHAR2(240),
  LOGIN_REQUIRE         VARCHAR2(1) DEFAULT 'Y' NOT NULL,
  ACCESS_CHECK          VARCHAR(1) DEFAULT 'Y'  NOT NULL,
  OBJECT_VERSION_NUMBER NUMBER DEFAULT 1        NOT NULL,
  REQUEST_ID            NUMBER DEFAULT -1,
  PROGRAM_ID            NUMBER DEFAULT -1,
  CREATION_DATE         DATE DEFAULT sysdate    NOT NULL,
  CREATED_BY            NUMBER DEFAULT -1       NOT NULL,
  LAST_UPDATED_BY       NUMBER DEFAULT -1       NOT NULL,
  LAST_UPDATE_DATE      DATE DEFAULT sysdate    NOT NULL,
  LAST_UPDATE_LOGIN     NUMBER,
  ATTRIBUTE_CATEGORY    VARCHAR2(30),
  ATTRIBUTE1            VARCHAR2(240),
  ATTRIBUTE2            VARCHAR2(240),
  ATTRIBUTE3            VARCHAR2(240),
  ATTRIBUTE4            VARCHAR2(240),
  ATTRIBUTE5            VARCHAR2(240),
  ATTRIBUTE6            VARCHAR2(240),
  ATTRIBUTE7            VARCHAR2(240),
  ATTRIBUTE8            VARCHAR2(240),
  ATTRIBUTE9            VARCHAR2(240),
  ATTRIBUTE10           VARCHAR2(240),
  ATTRIBUTE11           VARCHAR2(240),
  ATTRIBUTE12           VARCHAR2(240),
  ATTRIBUTE13           VARCHAR2(240),
  ATTRIBUTE14           VARCHAR2(240),
  ATTRIBUTE15           VARCHAR2(240)
);
COMMENT ON TABLE SYS_RESOURCE_B IS '功能资源表';
COMMENT ON COLUMN SYS_RESOURCE_B.RESOURCE_ID IS '表ID，主键，供其他表做外键';
COMMENT ON COLUMN SYS_RESOURCE_B.URL IS 'URL';
COMMENT ON COLUMN SYS_RESOURCE_B.TYPE IS '资源类型';
COMMENT ON COLUMN SYS_RESOURCE_B.NAME IS '资源名称';
COMMENT ON COLUMN SYS_RESOURCE_B.DESCRIPTION IS '描述';
COMMENT ON COLUMN SYS_RESOURCE_B.LOGIN_REQUIRE IS '是否需要登录';
COMMENT ON COLUMN SYS_RESOURCE_B.ACCESS_CHECK IS '是否权限校验';

ALTER TABLE SYS_RESOURCE_B
  ADD CONSTRAINT SYS_RESOURCE_B_PK PRIMARY KEY (RESOURCE_ID);
CREATE UNIQUE INDEX SYS_RESOURCE_B_U1 ON SYS_RESOURCE_B (URL);

CREATE SEQUENCE SYS_RESOURCE_B_S START WITH 10001;


