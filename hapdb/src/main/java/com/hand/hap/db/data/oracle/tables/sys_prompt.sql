  ATTRIBUTE10           VARCHAR2(240),
  ATTRIBUTE11           VARCHAR2(240),
  ATTRIBUTE12           VARCHAR2(240),
  ATTRIBUTE13           VARCHAR2(240),
  ATTRIBUTE14           VARCHAR2(240),
  ATTRIBUTE15           VARCHAR2(240)
);
COMMENT ON TABLE SYS_PROMPTS IS '界面显示文本表';
COMMENT ON COLUMN SYS_PROMPTS.PROMPT_ID IS '表ID，主键，供其他表做外键';
COMMENT ON COLUMN SYS_PROMPTS.PROMPT_CODE IS '文本编码';
COMMENT ON COLUMN SYS_PROMPTS.LANG IS '语言';
COMMENT ON COLUMN SYS_PROMPTS.DESCRIPTION IS '描述';

ALTER TABLE SYS_PROMPTS
  ADD CONSTRAINT SYS_PROMPTS_PK PRIMARY KEY (PROMPT_ID);
